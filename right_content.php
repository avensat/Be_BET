</ul>
<h3>Protect your card from damage</h3>
<p>If you want to protect your card from damage, put it in a plastic sleeve that fits into your wallet. </p>
<ul>
    <li>You are permitted to laminate your card, but please consider signing the Universal Donor Card section before you laminate it.  Remember to tell you family of your wishes to be an organ donor. </li>
</ul>
<h2><a id="report-abuse" name="report-abuse"></a>Report abuse or wrong use of a personal health care card or number</h2>
<p>It is important to keep your personal health care card and number protected. Ensure they are used only when you are obtaining publicly funded health services. </p>
<p>If you suspect or know of someone who is not using their personal health care card properly, or is using someone else’s card or number, please call our tip-line* at 1-866-278-5104.</p>
<p>Any information reported is confidential. Tip-line staff will not record any identifying information about the caller if a caller wishes to remain anonymous.</p>
<p>*<strong>Please note</strong>: The tip-line is only for calls relating to suspected/confirmed abuse of personal health care numbers and/or cards. All other inquires should be directed to our main customer inquiry line. </p> 
