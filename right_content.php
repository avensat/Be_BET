   <p>Remember to show your personal health care card along with photo identification when you visit a doctor’s office or hospital in Alberta. </p>
<ul>
    <li>Your photo identification and current address will be used to prove your identity. </li>
</ul>
<p>Up-to-date information is key to obtaining health services. Make sure your health care registration information is current. </p>
<h3>Why carry your card</h3>
<p>Showing your personal health care card and photo identification ensures eligible Albertans receive publicly-funded health services.</p>
<h2><a id="Where-to-show" name="Where-to-show"></a>Where to show your card</h2>
<p>Show your personal health care card every time you request or get <a  target="_self" href="/AHCIP/what-is-covered.html">insured medical services</a> from:</p>
<ul>
    <li>hospitals </li>
    <li>doctors’ offices </li>
    <li>laboratories </li>
    <li>physiotherapists </li>
    <li>pharmacies </li>
</ul>
<p>If you do not show your card or are not eligible, you may be asked to pay for health services before receiving them. </p>
<h3>Practitioner offices</h3>
<p>Patients may be asked to show their Alberta personal health care card and photo identification when visiting a practitioner office. The practitioner will submit a claim to the Alberta Health Care Insurance Plan directly for all insured services provided. If a provincial health care card is not shown or the individual is not eligible for coverage, they may be asked to pay for health services before receiving them. </p>
<h4>Examples of services a practitioner can bill a patient for – </h4>
<ol>
    <li>Any service not covered by the Alberta Health Care Insurance Plan. </li>
    <li>Any service provided to a patient if he/she does not have a valid Alberta health care card or number. </li>
    <li>Writing sick notes. </li>
    <li>Consultation with a patient by phone. </li>
    <li>Copying patient records. </li>
    <li>Filling out third party insurance or legal forms unless otherwise identified in the Schedule of Medical Benefits. </li>
    <li>Missed appointments. </li>
    <li>Refilling a prescription by phone. </li>
</ol>
<h2><a id="carry-it" name="carry-it"></a>Carry it everywhere you go</h2>
<p>It is a good idea to always carry your personal health care card. If you travel outside of Alberta or Canada, you should carry your card as well, just in case you need medical services when you’re away from home.</p>
<p>Alberta Health covers only some limited physician and hospital expenses outside Alberta. It is strongly recommended that Alberta residents carry private supplementary insurance when travelling outside of Alberta to cover unforeseen emergency care and transportation, as these costs may be significant. </p>
<ul>
    <li>For more information, <a  target="_self" href="/AHCIP/outside-coverage.html">read Health Care Coverage Outside Alberta</a> </li>
</ul>
<h2><a id="update-info" name="update-info"></a>Update your information</h2>
<p>Albertans must update their information if there is:</p>
<ul>
    <li>a name and/or marital status change </li>
    <li>an address change </li>
    <li>dependant status change, including adding a new dependant </li>
</ul>
<p>You can update your information by visiting an <a  target="_self" href="/AHCIP/registration-locations.html">authorized registry agent office</a> or completing the applicable <a  target="_self" href="/AHCIP/forms.html">Notice of Change form</a> and sending it to Alberta Health. </p>
<h2><a id="replace-card" name="replace-card"></a>How to replace a lost card</h2>
<p>Albertans can request a replacement card by telephone, in writing or in person. You must provide your: </p>
<ul>
    <li>full name </li>
    <li>mailing address </li>
    <li>telephone number </li>
    <li>personal health care number </li>
    <li>date of birth </li>
</ul>
<p>If you visit an authorized registry agent or <a  target="_self" href="/AHCIP/contact-AHCIP.html">telephone the AHCIP</a>, a replacement card is generally received within 1–2 weeks. If you mail or fax Alberta Health with a request, a replacement card is generally received within 4–6 weeks.</p>
<p>Do not request a replacement personal health care card via email. Due to the sensitive nature of the information required, do not send information or forms by email. Electronic submission is not available.</p>
<h2><a id="What-you-can-do" name="What-you-can-do"></a>What you can do with your card</h2>
<h3>Be an organ donor<img border="0" hspace="4" alt="Sign the back of your health card to be an organ donor" vspace="2" align="right"  src="/images/AHCIP-health-card-Donor.jpg" /></h3>
<p>You can sign the back of your personal health care card, which is your Universal Donor Card. This will help medical staff know of your intention to be an organ donor in case of your death. Remember to tell your family or next of kin of your wishes to be an organ donor.</p>
